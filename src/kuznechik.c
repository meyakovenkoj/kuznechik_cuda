void nonlinear_transform_direct128(uint8_t *target)
{
	uint8_t *p_end = target + BLOCK_SIZE;
	while (target != p_end)
	{
		*target = sbox[*target];
		target++;
	}
}

static uint8_t kuz_mul_gf256(uint8_t x, uint8_t y)
{
	uint8_t z;

	z = 0;
	while (y)
	{
		if (y & 1)
			z ^= x;
		x = (x << 1) ^ (x & 0x80 ? 0xC3 : 0x00);
		y >>= 1;
	}

	return z;
}

static void kuz_l(uint8_t *w)
{
	int i, j;
	uint8_t x;

	// 16 rounds
	for (j = 0; j < 16; j++)
	{

		// An LFSR with 16 elements from GF(2^8)
		x = *(w + 15); // since lvec[15] = 1

		for (i = 14; i >= 0; i--)
		{
			*(w + i + 1) = *(w + i);
			x ^= kuz_mul_gf256(*(w + i), linear_transform_coeff[i]);
		}
		*(w + 0) = x;
	}
}

void expand_key(uint8_t *key, uint8_t *extkey)
{
	memcpy(extkey, key, 16);		   // key1
	memcpy(extkey + 16, key + 16, 16); // key2
	uint8_t buf[16];
	uint8_t c[16];
	uint8_t x[16];
	uint8_t y[16];

	memcpy(x, key, 16);

	memcpy(y, key + 16, 16);

	for (int i = 1; i <= 32; i++)
	{

		// C Value
		memset(c, 0, 16);
		c[15] = i; // load round in lsb
		kuz_l(c);

		for (int k = 0; k < 16; k++)
		{
			buf[k] = c[k] ^ x[k];
		}
		nonlinear_transform_direct128(buf);

		kuz_l(buf);

		for (int k = 0; k < 16; k++)
		{
			buf[k] ^= y[k];
		}
		memcpy(y, x, 16);
		memcpy(x, buf, 16);

		if ((i & 7) == 0)
		{
			memcpy(extkey + (i >> 2) * 16, x, 16);		 // key 2i
			memcpy(extkey + ((i >> 2) + 1) * 16, y, 16); // key 2i+1
		}
	}
}

void create_ls_matrix(uint8_t *ls_matrix, uint8_t *key, uint8_t *rkey)
{
	expand_key(key, rkey);
	for (int i = 0; i != 256; ++i) {        //256 values
		for (int j = 0; j != 16; ++j) {     //16 rows
			uint8_t pi_i = sbox[i];
			for (int k = 0; k != 16; ++k) {
				ls_matrix[i*256 + j*16 + k] = kuz_mul_gf256(pi_i, linear_transform_matrix[j*16 + k]);
			}
		}
	}
}

